#include<stdio.h>

int main(){
    int total;
    scanf("%d", &total);
    for(int i=0;i<total;i++){
        int times,sum=0;
        scanf("%d", &times);
        int money[times];
        for(int j=0;j<times;j++){
            scanf("%d", &money[j]);
            sum+=money[j];
        }
        
        if(sum<=500){
            printf("Sheng has his idea.\n");
        }
        else{
            printf("Sheng has no idea.\n");
        }
    }
    
    return 0;
}
